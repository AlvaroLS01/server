package com.comerzzia.pos.services.promociones;

public class PromocionesBuilderException extends Exception {

	private static final long serialVersionUID = 1L;

	public PromocionesBuilderException() {
		super();
	}

	public PromocionesBuilderException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public PromocionesBuilderException(String message, Throwable cause) {
		super(message, cause);
	}

	public PromocionesBuilderException(String message) {
		super(message);
	}

	public PromocionesBuilderException(Throwable cause) {
		super(cause);
	}

}
