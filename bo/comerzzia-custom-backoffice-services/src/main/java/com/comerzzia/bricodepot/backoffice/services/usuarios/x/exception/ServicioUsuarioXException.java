package com.comerzzia.bricodepot.backoffice.services.usuarios.x.exception;

public class ServicioUsuarioXException extends Exception {

	private static final long serialVersionUID = -6843159860248373500L;

	public ServicioUsuarioXException() {
		super();
	}

	public ServicioUsuarioXException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ServicioUsuarioXException(String message, Throwable cause) {
		super(message, cause);
	}

	public ServicioUsuarioXException(String message) {
		super(message);
	}

	public ServicioUsuarioXException(Throwable cause) {
		super(cause);
	}

}
